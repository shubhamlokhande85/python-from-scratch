# Assignment_operators
'''these operator used to assign value to variables and some
time update the value of a variable by performing operations 
on its current value and then storing the result back in the
same variable '''

# 1) =
a = 10
print(10)
#output - 10

#2) += 
a += 1 # a = a + 1  here a = 10 
print(a)
# output - 11 

# 3) -=
a -= 2  # a = a - 2  here a = 11 
print(a)
# output - 9 


#4) *= 

a *= 3 # a = a*3  here a = 9 
print(a)
# output - 27  

#5) /=
a /= 3 # a = a /3 here a = 27
print(a)
#output - 9.0 # division returns values in floating numbers

#6) %= 
a %= 3 # a = a % 3 here a = 9.0 and % its return remainder 
print(a)
#output - 0.0 

# 7) //= 
# its return quotient with whole intger not in float 
b = 153
b //= 10 # b = b // 10 here b = 153 
print(b)
# output - 15 # its return intger insted of 15.3 

