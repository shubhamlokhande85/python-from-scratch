# bitwise operator 
'''bitwise operator in python are used to perform operations
directly on the binary (bit - level) representation of interfers '''

# binary - 0 and 1 

# 1) bitwise AND(&)
a = 2
b = 3 
print(a & b)
# output - 2

# 2) bitwise OR (|)
print(a | b)
# output - 3

# 3) bitwise XOR(^)
print(a ^ b)
# output - 1 

# 4) bitwise right shift (>>)
c = 4 
print(c >> 1)
# output - 2 

# 5 ) bitwise left shift (<<)
d = 4 
print(d << 1 )
# output - 8 

# 6) bitwise NOT (~)
print(~a)
# output_ -3